#include <vector>

// std is used as a namespace to make this code identical to a valid solution on Leetcode
using namespace std;

// "Solution" exists to make this code identical to a valid solution on Leetcode 
class Solution {
public:
vector<int> countBits(int n) {    
    vector<int> ans = {0};
    ans.reserve(n+1);
    
    if(n>0){
        ans.push_back(1);
    }
    
    int LoopLength=1;
    int Counter=1;
    
    for(int Index=0; Index<n-1; Index++){
        if(Counter == LoopLength){
            ans.push_back(1);
            Counter = 1;
            LoopLength *= 2;
        }else{
            ans.push_back(1+ans[Counter]);
            Counter++;
        }
    }
    
    return ans;
}
};


